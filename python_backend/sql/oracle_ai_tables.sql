-- 보드게임 AI 실험용 최소 테이블 3종입니다.
-- 실제 TXT 원문은 파일 시스템에 두고, DB에는 메타데이터/로그/캐시만 저장합니다.
-- Oracle 12c+ 환경을 기준으로 IDENTITY 컬럼을 사용했습니다.
-- 구버전 Oracle이라면 SEQUENCE + TRIGGER 방식으로 바꾸면 됩니다.

CREATE TABLE AI_GAME_DOCUMENT (
    DOCUMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GAME_ID NUMBER NOT NULL,
    DOCUMENT_TYPE VARCHAR2(20 CHAR) NOT NULL,
    DOCUMENT_TITLE VARCHAR2(200 CHAR) NOT NULL,
    FILE_NAME VARCHAR2(255 CHAR) NOT NULL,
    FILE_PATH VARCHAR2(500 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(1000 CHAR),
    ACTIVE_YN CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_AI_GAME_DOCUMENT_GAME
        FOREIGN KEY (GAME_ID) REFERENCES GAME_MASTER (GAME_ID),
    CONSTRAINT CK_AI_GAME_DOCUMENT_TYPE
        CHECK (DOCUMENT_TYPE IN ('RULEBOOK', 'WORLDVIEW')),
    CONSTRAINT CK_AI_GAME_DOCUMENT_ACTIVE
        CHECK (ACTIVE_YN IN ('Y', 'N')),
    CONSTRAINT UK_AI_GAME_DOCUMENT_FILE
        UNIQUE (GAME_ID, DOCUMENT_TYPE, FILE_PATH)
);

CREATE TABLE AI_REQUEST_LOG (
    REQUEST_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GAME_ID NUMBER NOT NULL,
    DOCUMENT_ID NUMBER,
    REQUEST_TYPE VARCHAR2(30 CHAR) NOT NULL,
    QUESTION_TEXT CLOB,
    MODEL_NAME VARCHAR2(100 CHAR) NOT NULL,
    REQUEST_HASH VARCHAR2(64 CHAR) NOT NULL,
    CACHE_HIT_YN CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
    RESPONSE_STATUS VARCHAR2(20 CHAR) NOT NULL,
    ERROR_MESSAGE VARCHAR2(2000 CHAR),
    RESPONSE_TIME_MS NUMBER(10),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_AI_REQUEST_LOG_GAME
        FOREIGN KEY (GAME_ID) REFERENCES GAME_MASTER (GAME_ID),
    CONSTRAINT FK_AI_REQUEST_LOG_DOCUMENT
        FOREIGN KEY (DOCUMENT_ID) REFERENCES AI_GAME_DOCUMENT (DOCUMENT_ID),
    CONSTRAINT CK_AI_REQUEST_LOG_TYPE
        CHECK (REQUEST_TYPE IN ('WORLDVIEW_EXPLAIN', 'RULEBOOK_QA')),
    CONSTRAINT CK_AI_REQUEST_LOG_CACHE
        CHECK (CACHE_HIT_YN IN ('Y', 'N')),
    CONSTRAINT CK_AI_REQUEST_LOG_STATUS
        CHECK (RESPONSE_STATUS IN ('SUCCESS', 'FAIL'))
);

CREATE TABLE AI_RESPONSE_CACHE (
    CACHE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GAME_ID NUMBER NOT NULL,
    DOCUMENT_ID NUMBER NOT NULL,
    REQUEST_TYPE VARCHAR2(30 CHAR) NOT NULL,
    QUESTION_TEXT CLOB,
    MODEL_NAME VARCHAR2(100 CHAR) NOT NULL,
    REQUEST_HASH VARCHAR2(64 CHAR) NOT NULL,
    RESPONSE_TEXT CLOB NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_AI_RESPONSE_CACHE_GAME
        FOREIGN KEY (GAME_ID) REFERENCES GAME_MASTER (GAME_ID),
    CONSTRAINT FK_AI_RESPONSE_CACHE_DOCUMENT
        FOREIGN KEY (DOCUMENT_ID) REFERENCES AI_GAME_DOCUMENT (DOCUMENT_ID),
    CONSTRAINT CK_AI_RESPONSE_CACHE_TYPE
        CHECK (REQUEST_TYPE IN ('WORLDVIEW_EXPLAIN', 'RULEBOOK_QA')),
    CONSTRAINT UK_AI_RESPONSE_CACHE_HASH
        UNIQUE (REQUEST_HASH)
);

CREATE INDEX IX_AI_GAME_DOCUMENT_GAME ON AI_GAME_DOCUMENT (GAME_ID);
CREATE INDEX IX_AI_GAME_DOCUMENT_TYPE ON AI_GAME_DOCUMENT (DOCUMENT_TYPE);

CREATE INDEX IX_AI_REQUEST_LOG_GAME ON AI_REQUEST_LOG (GAME_ID);
CREATE INDEX IX_AI_REQUEST_LOG_DOCUMENT ON AI_REQUEST_LOG (DOCUMENT_ID);
CREATE INDEX IX_AI_REQUEST_LOG_CREATED ON AI_REQUEST_LOG (CREATED_AT);
CREATE INDEX IX_AI_REQUEST_LOG_TYPE ON AI_REQUEST_LOG (REQUEST_TYPE);

CREATE INDEX IX_AI_RESPONSE_CACHE_GAME ON AI_RESPONSE_CACHE (GAME_ID);
CREATE INDEX IX_AI_RESPONSE_CACHE_DOCUMENT ON AI_RESPONSE_CACHE (DOCUMENT_ID);
